"""Dataset loading and episode resolution."""

from __future__ import annotations

import json
import logging
import random
from pathlib import Path
from typing import TYPE_CHECKING

from anvil_shared.splits import load_split_info
from lerobot.datasets.lerobot_dataset import LeRobotDataset

if TYPE_CHECKING:
    from .config import EvalConfig

log = logging.getLogger(__name__)


class EvaluationDataset:
    """Wrapper for LeRobotDataset to handle evaluation-specific logic."""

    def __init__(self, dataset_path: Path):
        self.dataset_path = dataset_path
        # We assume dataset is local for offline eval
        self.dataset = LeRobotDataset(repo_id="local", root=str(dataset_path))
        self.total_episodes = self.dataset.num_episodes
        self.joint_names = self._load_joint_names()

    def _load_joint_names(self) -> list[str]:
        """Read joint/action names from dataset metadata."""
        info_path = self.dataset_path / "meta" / "info.json"
        if not info_path.exists():
            # Fallback if meta/info.json is missing, try to get from dataset.features
            if "action" in self.dataset.features:
                return self.dataset.features["action"].get("names", [])
            return []

        info = json.loads(info_path.read_text())
        names = info.get("features", {}).get("action", {}).get("names", [])

        # Handle nested format: [{"motor_names": ["j1", "j2"]}, ...]
        if names and isinstance(names[0], dict):
            names = [n for group in names for n in group.get("motor_names", [])]

        return names

    def resolve_splits(self, anvil_cfg: dict, checkpoint_path: Path | None = None) -> dict[str, list[int]]:
        """Get episode lists per split from split_info.json, anvil_config, or fallback defaults."""
        ranges: dict[str, list[int]] = {}

        # 1. Try loading from split_info.json inside the checkpoint's pretrained_model dir
        if checkpoint_path:
            # Priority A: Directly inside pretrained_model (new standard)
            # Priority B: Job root (for older updated-trainer runs)
            for candidate in (
                checkpoint_path / "pretrained_model" / "split_info.json",
                checkpoint_path.parent.parent / "split_info.json",
            ):
                split_data = load_split_info(candidate)
                if split_data is not None:
                    log.info("[anvil-eval] Loaded split info from %s", candidate)
                    return {
                        "train": split_data.get("train_episodes", []),
                        "val": split_data.get("val_episodes", []),
                        "test": split_data.get("test_episodes", []),
                    }

        # 2. Priority 1: Explicit episode info from anvil_config.json (legacy fallback)
        for split_key in ("train", "val", "test"):
            cfg_key = f"{split_key}_episodes"
            if cfg_key in anvil_cfg and anvil_cfg[cfg_key] is not None:
                val = anvil_cfg[cfg_key]
                if isinstance(val, list) and len(val) > 0:
                    # Check if it's a range [start, end] or a full list
                    # anvil_trainer Phase 1 saved ranges [start, end]
                    # anvil_trainer updated now saves full lists
                    # We can disambiguate: if len=2 and it's a range, val[1] > val[0] + 1 usually
                    # but safest is checking if it covers all intermediate integers.
                    # Or just: if it was generated by the OLD code, it's a 2-element range.
                    if len(val) == 2 and val[1] > val[0] and (val[1] - val[0]) > 1:
                        # Fallback for OLD range format
                        ranges[split_key] = list(range(val[0], val[1] + 1))
                    else:
                        # Full list (or a very short 2-element split)
                        ranges[split_key] = val

        if ranges:
            return ranges

        # 3. Priority 2: Compute from split_ratio
        ratio = anvil_cfg.get("split_ratio", [8.0, 1.0, 1.0])
        return self._compute_split_ranges(ratio)

    def _compute_split_ranges(self, ratio: list[float]) -> dict[str, list[int]]:
        """Compute split ranges from ratio."""
        total_r = sum(ratio)
        if total_r <= 0:
            return {"train": list(range(self.total_episodes))}

        n_test = (
            max(1, round(self.total_episodes * ratio[2] / total_r))
            if len(ratio) > 2 and ratio[2] > 0
            else 0
        )
        n_val = max(1, round(self.total_episodes * ratio[1] / total_r)) if ratio[1] > 0 else 0
        n_train = self.total_episodes - n_val - n_test

        ranges: dict[str, list[int]] = {}
        if n_train > 0:
            ranges["train"] = list(range(0, n_train))
        if n_val > 0:
            ranges["val"] = list(range(n_train, n_train + n_val))
        if n_test > 0:
            ranges["test"] = list(range(n_train + n_val, self.total_episodes))
        return ranges

    def get_episode_frames(self, episode_idx: int) -> list[int]:
        """Return dataset indices for all frames in a given episode."""
        # LeRobotDataset might have more efficient ways, but this is robust
        episode_col = self.dataset.hf_dataset["episode_index"]
        return [i for i, ep in enumerate(episode_col) if int(ep) == episode_idx]


def get_episode_indices(
    split_info: dict[str, list[int]], config: EvalConfig
) -> list[tuple[int, str]]:
    """Resolve which episodes to evaluate and their split labels.

    Evaluates config.split ('train', 'val', 'test', or 'all').
    Samples at most config.num_episodes per split.
    """
    rng = random.Random(config.seed)
    splits_to_run = (
        [s for s in ("train", "val", "test") if s in split_info]
        if config.split == "all"
        else [config.split]
    )

    result: list[tuple[int, str]] = []
    for sn in splits_to_run:
        eps = split_info.get(sn, [])
        n = min(len(eps), config.num_episodes)
        sampled = rng.sample(eps, n)
        result.extend((ep, sn) for ep in sampled)

    result.sort(key=lambda x: x[0])
    return result
